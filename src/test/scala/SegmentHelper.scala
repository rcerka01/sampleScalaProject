  def tupacPageBaseUrl = "https://music-tupac-production.test.tools.co.uk/music/records/"

  def get(url: String,
          connectTimeout: Int = 10000,
          readTimeout: Int = 10000,
          requestMethod: String = "GET"): String = {
    import java.net.{URL, HttpURLConnection}
    val connection =
      (new URL(url)).openConnection.asInstanceOf[HttpURLConnection]
    connection.setConnectTimeout(connectTimeout)
    connection.setReadTimeout(readTimeout)
    connection.setRequestMethod(requestMethod)
    val inputStream = connection.getInputStream
    val content = Source.fromInputStream(inputStream).mkString
    if (inputStream != null) inputStream.close
    content
  }

  def verify(verifyPids: List[String]) = {
    val verified = pids map { pid =>
      try {
        val res = get(s"${tupacPageBaseUrl}${pid}")

        def findLink(link: String, res: String) = {
          val start = res.indexOf(link)
          val end = res.indexOf("\">")

          val container = res.substring(start, start + end)

          val startOfUrl = container.indexOf("href=")
          val endOfUrl = container.indexOf("\">")
          val url = container.substring(startOfUrl + 5, endOfUrl)
          url
        }

        val apple = findLink("Apple Web", res)
        val spotify = findLink("Spotify Web", res)
        (pid -> (apple, spotify))

      } catch {
        case e: Throwable => {
          println(e)
          (pid -> "")
        }
      }
    }

    verified foreach println

    println(verified.size)
  }

  def generateSql(pids: List[String]): Unit = {

    val status: Map[String, Int] = pids map { pid =>
      try {
        val res = get(s"${tupacPageBaseUrl}${pid}")
        if (res.trim.nonEmpty) (pid -> 200)
        else (pid -> 100)
      } catch {
        case e: Throwable => {
          println(e)
          (pid -> 0)
        }
      }
    } toMap

    def segment(segmentId: String, pid: String) =
      s"insert into programmes.segment(pid, isrc_id, record_id, type, duration, title, sequencer_int) values ('$segmentId', 'GBLLT1505852','$pid', 'music', null, 'abc', null);"

    def version(id: String, version: String) =
      s"insert into programmes.versions(pid, version_id) values ('$id', '$version');"

    def segmentEvent(id: String, segment: String, version: String) =
      s"insert into programmes.segment_event(pid, segment_id, version_id) values ('$id', '$segment', '$version');"

    val id: Map[String, Int] = pids.zipWithIndex toMap

    val sql: Map[String, (String, String, String)] = status filter { r =>
      r._2 != 0
    } map { data =>
      val pid = data._1
      val segmentId = s"5309seg0${id(pid)}"
      val versionId = s"1234560${id(pid)}"
      val versionSegmentId = s"5309ver0${id(pid)}"
      val segmentEventId = s"5309ent0${id(pid)}"
      val sql: (String, String, String) = (
        segment(segmentId, pid),
        version(versionId, versionSegmentId),
        segmentEvent(segmentEventId, segmentId, versionSegmentId)
      )
      (pid, sql)
    }

    sql foreach { s =>
      println(s._2._1)
      println(s._2._2)
      println(s._2._3)
    }

  }

